package app.jam.jam.methods;

public class Cryptography {

    /**
     * This method encrypts the given {@code String}.
     *
     * @param plainText the plaintext to encrypt
     * @return The encrypted cipher plainText
     */
    public static String encrypt(String plainText) {
        return plainText;
    }

    /**
     * This method decrypts the given {@code String}.
     *
     * @param cipherCode the cipherCode to decrypt
     * @return The decrypted plaintext
     */
    public static String decrypt(String cipherCode) {
        return cipherCode;
    }

}
